# List of places to travel to
va_list = ['Mumbasa', 'Hong Kong', 'Ethiopia']

print("Original Favourite vacation list")
print(va_list)
print("\n")
print("Sorted vacation list")
print(sorted(va_list))

va_list.reverse()
print("\nReversed vacation list")
print(va_list)

va_list.reverse()
print("\nRe-reversed vacation list")
print(va_list)

va_list.sort()
print("\nSort vacation list")
print(va_list)

va_list.sort(reverse=True)
print("\nsort_reverse vacation list")
print(va_list)